func mockBuilder() *servermock.Builder[*DNSProvider] {
	return servermock.NewBuilder(
		func(server *httptest.Server) (*DNSProvider, error) {
			config := NewDefaultConfig()
			config.Token = "secret"
			config.HTTPClient = server.Client()

			p, err := NewDNSProviderConfig(config)
			if err != nil {
				return nil, err
			}

			p.client.BaseURL, _ = url.Parse(server.URL)

			return p, nil
		},
		servermock.CheckHeader().
			WithJSONHeaders().
			WithAuthorization("Bearer secret"),
	)
}

func TestDNSProvider_Present(t *testing.T) {
	provider := mockBuilder().
		Route("POST /ns/zone/list",
			servermock.ResponseFromInternal("zone_list.json"),
		).
		Route("POST /ns/record/create",
			servermock.ResponseFromInternal("record_create.json"),
			servermock.CheckRequestJSONBodyFromInternal("record_create-request.json"),
		).
		Build(t)

	err := provider.Present(t.Context(), "example.com", "abc", "123d==")
	require.NoError(t, err)
}

func TestDNSProvider_CleanUp(t *testing.T) {
	provider := mockBuilder().
		Route("POST /ns/record/delete",
			servermock.ResponseFromInternal("record_delete.json"),
			servermock.CheckRequestJSONBodyFromInternal("record_delete-request.json"),
		).
		Build(t)

	provider.zoneIDs["abc"] = "01DXF6DT00ZZZHQPP3JFJS8VEE"
	provider.recordIDs["abc"] = 435

	err := provider.CleanUp(t.Context(), "example.com", "abc", "123d==")
	require.NoError(t, err)
}
